import {React,useState} from "react";
import jwt_decode from "jwt-decode";
import { UserService } from "../../Services/UserService";
import bcrypt from "bcryptjs/dist/bcrypt";


export default function Register() {
  const [selectedFile, setSelectedFile] = useState();
	const [user, setUser] = useState({});
   
	const changeHandler = (event) => {
		setSelectedFile(event.target.files[0]);
	
	};

  const handleSubmit = (event) => {
    // Prevent page reload
    event.preventDefault();
    
    const agree = document.getElementById("customCheckLogin");
    if(agree.checked){

      const full_name = event.target.full_name.value;
      const email = event.target.email.value;
      const password = event.target.password.value;
     
      setUser({
        full_name : full_name,
        email: email,
        password: password,
        photo : "",
        role : "user",
      });
      var formData = new FormData();
      formData.append('file', selectedFile,selectedFile.name);
      formData.append('userInfo', JSON.stringify(user));
     
      UserService.register(formData).then(async response => {
      
        const token = response.data;
  
        sessionStorage.setItem("auth-token", token);
        var user = jwt_decode(token);
  
        const salt = await bcrypt.genSalt(10);
        const HashRole = await bcrypt.hash(user.role, salt)
        
  
        sessionStorage.setItem("role", HashRole);
        if (user.role === 'admin') {
          window.location.href = '/admin';
        } else {
          window.location.href = '/user';
        }
  
      }).catch(error => {
        console.log(error);
      })
    }else{
      console.log("You must agree to the terms and conditions");
    }
  
  };
  return (
    <>
      <div className="container mx-auto px-4 h-full">
        <div className="flex content-center items-center justify-center h-full">
          <div className="w-full lg:w-6/12 px-4">
            <div className="relative flex flex-col min-w-0 break-words w-full mb-6 shadow-lg rounded-lg bg-blueGray-200 border-0">
              <div className="rounded-t mb-0 px-6 py-6">
              
                <div className="btn-wrapper text-center">
                
                </div>
                <hr className="mt-6 border-b-1 border-blueGray-300" />
              </div>
              <div className="flex-auto px-4 lg:px-10 py-10 pt-0">
                <div className="text-blueGray-400 text-center mb-3 font-bold">
                  <small>Or sign up with credentials</small>
                </div>
                <form onSubmit={handleSubmit} >
                  <div className="relative w-full mb-3">
                    <label
                      className="block uppercase text-blueGray-600 text-xs font-bold mb-2"
                      htmlFor="grid-password"
                    >
                      Name
                    </label>
                    <input
                      type="text"
                      name="full_name"
                      className="border-0 px-3 py-3 placeholder-blueGray-300 text-blueGray-600 bg-white rounded text-sm shadow focus:outline-none focus:ring w-full ease-linear transition-all duration-150"
                      placeholder="Name"
                    />
                  </div>

                  <div className="relative w-full mb-3">
                    <label
                      className="block uppercase text-blueGray-600 text-xs font-bold mb-2"
                      htmlFor="grid-password"
                    >
                      Email
                    </label>
                    <input
                      type="email"
                      name="email"
                      className="border-0 px-3 py-3 placeholder-blueGray-300 text-blueGray-600 bg-white rounded text-sm shadow focus:outline-none focus:ring w-full ease-linear transition-all duration-150"
                      placeholder="Email"
                    />
                  </div>

                  <div className="relative w-full mb-3">
                    <label
                      className="block uppercase text-blueGray-600 text-xs font-bold mb-2"
                      htmlFor="grid-password"
                    >
                      Password
                    </label>
                    <input
                      type="password"
                      name="password"
                      className="border-0 px-3 py-3 placeholder-blueGray-300 text-blueGray-600 bg-white rounded text-sm shadow focus:outline-none focus:ring w-full ease-linear transition-all duration-150"
                      placeholder="Password"
                    />
                  </div>
                  <div className="relative w-full mb-3">
                    <label
                      className="block uppercase text-blueGray-600 text-xs font-bold mb-2"
                      htmlFor="grid-password"
                    >
                      Photo
                    </label>
                    <input
                      onChange={changeHandler}
                      type="file"
                      name="photo"
                      className="border-0 px-3 py-3 placeholder-blueGray-300 text-blueGray-600 bg-white rounded text-sm shadow focus:outline-none focus:ring w-full ease-linear transition-all duration-150"
                      placeholder="file"
                    />
                  </div>
                  <div>
                    <label className="inline-flex items-center cursor-pointer">
                      <input
                        id="customCheckLogin"
                        type="checkbox"
                        className="form-checkbox border-0 rounded text-blueGray-700 ml-1 w-5 h-5 ease-linear transition-all duration-150"
                      />
                      <span className="ml-2 text-sm font-semibold text-blueGray-600">
                        I agree with the{" "}
                        <a
                          href="#pablo"
                          className="text-lightBlue-500"
                          onClick={(e) => e.preventDefault()}
                        >
                          Privacy Policy
                        </a>
                      </span>
                    </label>
                  </div>

                  <div className="text-center mt-6">
                    <button
                      className="bg-blueGray-800 text-white active:bg-blueGray-600 text-sm font-bold uppercase px-6 py-3 rounded shadow hover:shadow-lg outline-none focus:outline-none mr-1 mb-1 w-full ease-linear transition-all duration-150"
                      type="submit"
                    >
                      Create Account
                    </button>
                  </div>
                </form>
              </div>
            </div>
          </div>
        </div>
      </div>
    </>
  );
}
